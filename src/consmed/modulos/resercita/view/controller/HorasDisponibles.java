package consmed.modulos.resercita.view.controller;

public class HorasDisponibles {
	private String hora;
	private String time;
	public HorasDisponibles(String hora, String time) {
		this.hora = hora;
		this.time=time;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
	
	
	
}
